/**
 * Build inline SVG markup for an icon.
 * @param {string} name  key from ICON_PATHS
 * @param {Object} [opts]
 * @param {number} [opts.size=24]
 * @param {string} [opts.cls='']  extra class names
 * @returns {string} svg markup (empty string if name unknown)
 */
export function iconSVG(name, { size = 24, cls = "" } = {}){
  const p = ICON_PATHS[name];
  if(!p) return "";
  return `<svg class="icon${cls ? " " + cls : ""}" viewBox="0 0 24 24" width="${size}" height="${size}" `
    + `fill="none" stroke="currentColor" stroke-width="2" stroke-linecap="round" `
    + `stroke-linejoin="round" aria-hidden="true">${p}</svg>`;
}
